'use strict';

/**
 * core.cjs — Core initialization and project scaffolding
 *
 * Handles .xiro/ directory creation and project initialization.
 */

const fs = require('fs');
const path = require('path');
const { getXiroRoot, saveConfig, DEFAULT_CONFIG } = require('./config.cjs');
const { initStateMd } = require('./state.cjs');
const { notifyEvent } = require('./memory-bridge.cjs');

/**
 * Initialize a new xiro project.
 * Creates .xiro/ directory with all required scaffolding.
 */
function initProject(featureName, opts) {
  const cwd = opts?.cwd || process.cwd();
  const xiroRoot = getXiroRoot(cwd);

  if (fs.existsSync(xiroRoot)) {
    return {
      success: false,
      message: `.xiro/ already exists. Use /xiro resume to continue.`,
    };
  }

  // Create directory structure
  const dirs = [
    xiroRoot,
    path.join(xiroRoot, 'phases'),
    path.join(xiroRoot, 'evidence'),
    path.join(xiroRoot, 'evidence', 'gold'),
    path.join(xiroRoot, 'state'),
    path.join(xiroRoot, 'ledger'),
    path.join(xiroRoot, 'memory'),
    path.join(xiroRoot, 'memory', 'inbox'),
  ];

  for (const dir of dirs) {
    fs.mkdirSync(dir, { recursive: true });
  }

  // Write config
  saveConfig(DEFAULT_CONFIG, cwd);

  // Write STATE.md
  initStateMd(featureName, cwd);

  // Write empty ROADMAP.md
  fs.writeFileSync(
    path.join(xiroRoot, 'ROADMAP.md'),
    `# xiro Roadmap: ${featureName}\n\n_Generated by xiro_\n`,
    'utf-8',
  );

  // Write empty shared.md
  fs.writeFileSync(
    path.join(xiroRoot, 'shared.md'),
    `# Shared Knowledge\n\n_Worker-discovered gotchas. Append-only during a phase._\n`,
    'utf-8',
  );

  // Write empty gold-tests.md
  fs.writeFileSync(
    path.join(xiroRoot, 'gold-tests.md'),
    `# Gold Tests: ${featureName}\n\nKiller scenarios that prove the feature works. Add-only — never delete.\n`,
    'utf-8',
  );

  // Write empty criteria-lock.json
  fs.writeFileSync(
    path.join(xiroRoot, 'criteria-lock.json'),
    JSON.stringify({ phases: {}, cannot_verify: [] }, null, 2) + '\n',
    'utf-8',
  );

  // Write empty verification-state.json
  fs.writeFileSync(
    path.join(xiroRoot, 'verification-state.json'),
    JSON.stringify({ tasks: {}, phases: {} }, null, 2) + '\n',
    'utf-8',
  );

  // Write initial run state
  const { saveRunState } = require('./state.cjs');
  saveRunState({
    active_feature: featureName,
    active_phase: null,
    status: 'initialized',
    tasks: {},
    attempt_counts: {},
    consecutive_verifier_failures: 0,
  }, cwd);

  // Initialize ledger
  notifyEvent({
    type: 'FEATURE_CREATED',
    feature: featureName,
    timestamp: new Date().toISOString(),
  }, cwd);

  return {
    success: true,
    message: `xiro project initialized: ${featureName}`,
    xiroRoot,
  };
}

/**
 * Check if a xiro project exists.
 */
function projectExists(cwd) {
  return fs.existsSync(getXiroRoot(cwd));
}

/**
 * Get project info.
 */
function getProjectInfo(cwd) {
  const xiroRoot = getXiroRoot(cwd);
  if (!fs.existsSync(xiroRoot)) return null;

  const { loadConfig } = require('./config.cjs');
  const { loadRunState } = require('./state.cjs');
  const { listPhases, getActivePhase } = require('./phase.cjs');

  const config = loadConfig(cwd);
  const runState = loadRunState(cwd);
  const phases = listPhases(cwd);
  const activePhase = getActivePhase(cwd);

  // Read spec anchor if exists
  const anchorPath = path.join(xiroRoot, 'spec-anchor.md');
  const specAnchor = fs.existsSync(anchorPath)
    ? fs.readFileSync(anchorPath, 'utf-8')
    : null;

  return {
    feature: runState.active_feature,
    status: runState.status,
    activePhase: activePhase?.number || null,
    totalPhases: phases.length,
    phases: phases.map(p => ({
      number: p.number,
      name: p.name,
    })),
    specAnchor,
    config,
  };
}

/**
 * Run a health check on the .xiro/ directory.
 */
function healthCheck(cwd) {
  const xiroRoot = getXiroRoot(cwd);
  const issues = [];

  if (!fs.existsSync(xiroRoot)) {
    return { healthy: false, issues: ['.xiro/ directory not found'] };
  }

  // Check required files
  const requiredFiles = [
    'config.json',
    'STATE.md',
    'criteria-lock.json',
  ];

  for (const file of requiredFiles) {
    if (!fs.existsSync(path.join(xiroRoot, file))) {
      issues.push(`Missing required file: ${file}`);
    }
  }

  // Check required directories
  const requiredDirs = ['phases', 'evidence', 'state', 'ledger', 'memory'];
  for (const dir of requiredDirs) {
    if (!fs.existsSync(path.join(xiroRoot, dir))) {
      issues.push(`Missing required directory: ${dir}`);
    }
  }

  // Validate config
  try {
    const { loadConfig } = require('./config.cjs');
    loadConfig(cwd);
  } catch (e) {
    issues.push(`Invalid config.json: ${e.message}`);
  }

  // Check ledger integrity
  const ledgerPath = path.join(xiroRoot, 'ledger', 'events.ndjson');
  if (fs.existsSync(ledgerPath)) {
    const content = fs.readFileSync(ledgerPath, 'utf-8');
    const lines = content.trim().split('\n').filter(l => l.trim());
    for (let i = 0; i < lines.length; i++) {
      try {
        JSON.parse(lines[i]);
      } catch {
        issues.push(`Malformed ledger entry at line ${i + 1}`);
      }
    }
  }

  return {
    healthy: issues.length === 0,
    issues,
  };
}

module.exports = {
  initProject,
  projectExists,
  getProjectInfo,
  healthCheck,
};
